constexpr int MOD = 998244353, INF = 1e9;

void solve()
{
    int n, l, r;
    cin >> n >> l >> r;
    vector<int> a(n + 1);
    for (int i = 0; i <= n;i++)
        cin >> a[i];
    vector<int> dp(n + 1,-INF);
    dp[0] = a[0];
    deque<int> maxre;
    int ans = -INF;
    for (int i = l; i <= n;i++)
    {
        if(i - l >= 0)
        {
            //判断能不能考虑就看之前处理是dp[i - l]有没有到达过，没到达过就不能处理
            if(dp[i - l] != INF)
            {
                while(!maxre.empty() && dp[maxre.back()] <= dp[i - l])
                maxre.pop_back();
                maxre.push_back(i - l);
            }
            if(i > r && maxre.front() < i - r)
                maxre.pop_front();
            chmax(dp[i], dp[maxre.front()] + a[i]);
        }
        if(i + r > n)
            chmax(ans, dp[i]);
    }
    cout << ans;
}

signed lyc_fan_club()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    // cin >> T;
    while(T--)
        solve();
    return 0;
}