//这题我想了一下，对于每个怪物，我们维护一个新的数据c[i]，在第i个怪物的前一个怪物爆炸后还需要打多少发子弹才能让这个怪物爆炸。假设我先打第i个怪物。我们会很自然的考虑顺着打下去，那么就是a[i]加上除了i本身的所以c[k]，这样我们只要遍历所有的i就可以了.
void solve()
{
    int n;
    cin >> n;
    vector<pll> a(n);
    for (int i = 0; i < n;i++)
        cin >> a[i].first >> a[i].second;
    vector<i64> c(n);
    i64 sum = 0;
    for (int i = 0; i < n;i++)
    {
        int lst = (i - 1 + n) % n;
        c[i] = max(0ll, a[i].first - a[lst].second);
        sum += c[i];
    }
    i64 ans = 2e18;
    for (int i = 0; i < n;i++)
        chmin(ans, a[i].first + sum - c[i]);
    cout << ans << "\n";
}

signed lyc_fan_club()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    cin >> T;
    while(T--)
        solve();
    return 0;
}