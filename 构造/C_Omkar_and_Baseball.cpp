//如果只有一个连续区间是错位的，我们实际上只需要一次就够了
//否侧，需要两次，我们需要通过前缀和后缀进行查找
void solve()
{
    int n;
    cin >> n;
    vector<int> a(n + 1, 0);
    for (int i = 1; i <= n;i++)
        cin >> a[i];
    if(is_sorted(a.begin() + 1, a.end()))
        cout << 0 << "\n";
    else 
    {
        int r = -1, l = -1;
        for (int i = 1; i <= n;i++)
        {
            if(a[i] != i)
            {
                l = i;
                break;
            }
        }
        for (int i = n; i >= 1;i--)
        {
            if(a[i] != i)
            {
                r = i;
                break;
            }
        }   
        for (int i = l + 1; i < r;i++)
        {
            if(a[i] == i)
            {
                cout << 2 << "\n";
                return;
            }
        }
        cout << 1 << "\n";
    }
}

signed lyc_fan_club()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    cin >> T;
    while(T--)
        solve();
    return 0;
}