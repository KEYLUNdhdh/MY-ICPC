struct DSU
{
    vector<int> f, siz;
    int part;
    DSU() {};
    DSU(int n)
    {
        init(n);
    }
    //input n,open n + 1
    void init(int n)
    {
        f.resize(n + 1);
        iota(f.begin(), f.end(), 0);
        siz.assign(n + 1, 1);
        part = n;
    }
    int find(int x)
    {
        while(x != f[x])
            x = f[x] = f[f[x]];
        return x;
    }
    bool same(int x,int y)
    {
        return find(x) == find(y);
    }
    bool merge(int x,int y)
    {
        x = find(x);
        y = find(y);
        if(x == y)
            return false;
        if(siz[x] < siz[y])
            swap(x, y);
        siz[x] += siz[y];
        f[y] = x;
        part--;
        return true;
    }
    int size(int x)
    {
        return siz[find(x)];
    }
};

struct cir
{
    i64 x, y, r;
};

void solve()
{
    i64 n, h;
    cin >> n >> h;
    DSU d(n + 1);
    vector<cir> a(n + 1);
    for (int i = 1; i <= n;i++)
        cin >> a[i].x >> a[i].y >> a[i].r;
    auto check = [&](cir &p, cir &q) -> bool
    {
        i64 dis = (p.x - q.x) * (p.x - q.x) + (p.y - q.y) * (p.y - q.y);
        i64 rm = q.r + p.r;
        if(dis <= rm * rm)
            return true;
        else
            return false;
    };
    for (int i = 1; i <= n;i++)
        for (int j = i + 1; j <= n;j++)
        {
            if(check(a[i], a[j]))
            {
                d.merge(i, j);
            }
        }
    i64 up = h;
    i64 lw = 0;
    for (int i = 1; i <= n;i++)
    {
        if(a[i].y <= a[i].r + lw)
            d.merge(0, i);
    }
    for (int i = 1; i <= n;i++)
    {
        if(up - a[i].y <= a[i].r)
            d.merge(n + 1, i);
    }
    if(d.find(0) == d.find(n + 1))
        cout << "Yes\n";
    else
        cout << "No\n";
}

signed lyc_fan_club()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    // cin >> T;
    while(T--)
        solve();
    return 0;
}