constexpr int MOD = 998244353, INF = 1e9;

void solve()
{
    int x, y, t;
    cin >> x >> y >> t;
    string s;
    cin >> s;
    char nx, ny;
    if(x >= 0)
        nx = 'R';
    else
        nx = 'L';
    if(y >= 0)
        ny = 'U';
    else
        ny = 'D';
    int cntx = 0, cnty = 0;
    int fx = 0, fy = 0;
    if(x == 0 && y == 0)
    {
        cout << 0 << "\n";
        return;
    }
    for (int i = 0;i < t;i++)
    {
        if(s[i] == nx)
            cntx++;
        if(cntx >= abs(x))
            fx = 1;
        if(s[i] == ny)
            cnty++;
        if(cnty >= abs(y))
            fy = 1;
        if(fx && fy)
        {
            cout << i + 1 << "\n";
            return;
        }
    }
    cout << -1 << "\n";
}

signed lyc_fan_club()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    cin >> T;
    while(T--)
        solve();
    return 0;
}