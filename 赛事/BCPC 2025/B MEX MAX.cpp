constexpr int MOD = 123456789, INF = 1e9;

void solve()
{
    int n;
    cin >> n;
    int f = 0;
    vector<int> cnt(n + 2, 0);
    for (int i = 0; i < n;i++)
    {
        int x;
        cin >> x;
        if (x > 0)
            f = 1;
        if(x <= n)
            cnt[x]++;
    }
    vector<i64> p(n + 1, 1);
    for (int i = 1; i <= n;i++)
        p[i] = (p[i - 1] * 2) % MOD;
    i64 ans = 0;
    if(cnt[0] > 0 && f)
        ans++;
    i64 prefix = 1;
    for (int v = 1; v <= n;v++)
    {
        if(cnt[v - 1] == 0)
            break;
        i64 cur = (p[cnt[v - 1]] - 1) % MOD;
        prefix = (prefix * cur) % MOD;
        if(prefix == 0)
            break;
        if(cnt[v] > 0)
            ans = (ans + prefix) % MOD;
    }
    cout << ans;
}

signed lyc_fan_club()
{
    ios::sync_with_stdio(0);
    cin.tie(0);
    int T = 1;
    // cin >> T;
    while(T--)
        solve();
    return 0;
}